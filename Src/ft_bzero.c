#include "../Includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptr_s;

	i = 0;
	ptr_s = (char *)s;
	while (i < n)
	{
		ptr_s[i] = '\0';
		i++;
	}
	return ;
}
